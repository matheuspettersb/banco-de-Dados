
CREATE TABLE SALGRADE (
  GRADE INTEGER UNSIGNED  NOT NULL,
  LOSAL FLOAT(8,2)  NULL  ,
  HISAL FLOAT(8,2)  NULL    ,
PRIMARY KEY(GRADE));


CREATE TABLE DEPT (
  DEPTNO INTEGER UNSIGNED  NOT NULL,
  DNAME VARCHAR(14)  NULL  ,
  LOC VARCHAR(13)  NULL    ,
PRIMARY KEY(DEPTNO));


CREATE TABLE EMP (
  EMPNO INTEGER UNSIGNED  NOT NULL  ,
  ENAME VARCHAR(10)  NULL  ,  
  JOB VARCHAR(9)  NULL  ,  
  MGR INTEGER UNSIGNED  NULL  ,
  HIREDATE DATE  NULL  ,
  SAL FLOAT(8,2)  NULL  ,
  COMM FLOAT(8,2)  NULL    ,
  DEPTNO INTEGER UNSIGNED  NOT NULL  ,
  PRIMARY KEY(EMPNO)  ,
  FOREIGN KEY(DEPTNO)
    REFERENCES DEPT(DEPTNO),
  FOREIGN KEY(MGR)
    REFERENCES EMP(EMPNO)
);

